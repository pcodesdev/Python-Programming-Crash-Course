#  Avoiding apostrophes in strings
# # If you want to include an apostrophe in a string, you can use double quotes to define the string.
# # This way, the apostrophe will not be treated as a string delimiter. For example:    # message = "I don't like Python"   # print(message)   
# # If you want to use single quotes, you can escape the apostrophe with a backslash.
# # For example: message = 'I don\'t like Python' print(message)
# 
# # Using double quotes to define a string with an apostrophe
message = "I don't like Python"
print(message)  # I don't like Python 
message = 'I don\'t like Python'
print(message)  # I don't like Python               